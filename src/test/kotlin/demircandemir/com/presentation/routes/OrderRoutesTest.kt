    @Test
    fun `POST _api_orders should create a new order when user creates own order`() {
        testApplication {
            application {
                testModule()
            }

            val orderRequest = CreateOrderRequest(
                userId = testUserId, // Creating order for user 1
                addressId = 1,
                paymentMethod = "Credit Card",
                items = listOf(CreateOrderItemRequest(productId = 1, quantity = 2)),
                shippingFee = "10.00"
            )

            coEvery { mockUserRepository.getUserById(testUserId) } returns mockk(relaxed = true)
            coEvery { mockUserRepository.getAddressById(1) } returns mockk(relaxed = true)
            coEvery { mockProductRepository.getProductById(1) } returns mockk { coEvery { price } returns BigDecimal("50.00") }
            coEvery { mockOrderRepository.create(any()) } returns Result.success(testOrder) // Assume creation returns our testOrder
            coEvery { mockOrderItemRepository.createMany(any()) } returns Result.success(listOf(testOrderItem))

            val response = client.post("/api/orders") {
                contentType(ContentType.Application.Json)
                jwtAuth() // Use default token for user 1
                setBody(Json.encodeToString(orderRequest))
            }

            assertEquals(HttpStatusCode.Created, response.status)
            assertTrue(response.bodyAsText().contains("Credit Card"))
        }
    }

