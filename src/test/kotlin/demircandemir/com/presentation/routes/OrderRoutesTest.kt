class OrderRoutesTest {
    private val mockOrderRepository: OrderRepository = mockk()
    private val mockOrderItemRepository: OrderItemRepository = mockk()
    private val mockProductRepository: ProductRepository = mockk()
    private val mockUserRepository: UserRepository = mockk()

    private val json = Json {
        prettyPrint = true
        isLenient = true
        ignoreUnknownKeys = true
        serializersModule = appSerializersModule
    }

    // Test User and JWT Setup
    private val testUserId = 1
    private val testUserRole = UserRole.CUSTOMER // Default role for most tests
    private val adminUserRole = UserRole.ADMIN

    private val jwtConfig = JwtConfig(ApplicationConfig("application-test.conf"))

    private fun createTestToken(userId: Int = testUserId, role: UserRole = testUserRole): String {
        // Create a minimal User object just for token generation
        val user = User(
            id = userId,
            email = "test@test.com",
            passwordHash = "hashedPassword",
            firstName = "Test",
            lastName = "User",
            phoneNumber = null,
            role = role,
            status = AccountStatus.ACTIVE,
            registrationDate = LocalDateTime.now(),
            lastLoginDate = null,
            verificationToken = null,
            passwordResetToken = null,
            passwordResetTokenExpiry = null,
            failedLoginAttempts = 0,
            lastFailedLoginAttempt = null
        )
        return jwtConfig.generateAccessToken(user)
    }

    private val defaultTestToken = createTestToken()
    private val adminTestToken = createTestToken(role = adminUserRole)

    // Test Order Data
    private val testOrder = Order(
        id = 1,
        userId = testUserId, // Associated with the default test user ID
        addressId = 1,
        totalAmount = BigDecimal("100.00"),
        orderDate = LocalDateTime.now(),
        paymentMethod = "Credit Card",
        orderStatus = OrderStatus.Pending,
        trackingNumber = null,
        shippingFee = BigDecimal("10.00")
    )

    private val testOrderItem = OrderItem(
        id = 1,
        orderId = 1,
        productId = 1,
        quantity = 2,
        unitPrice = BigDecimal("50.00"),
        sizeId = null,
        colorId = null
    )

    private fun Application.testModule() {
        install(ContentNegotiation) {
            json(json)
        }

        install(Authentication) {
            jwt("auth-jwt") {
                verifier(jwtConfig.verifier)
                validate { credential ->
                    // Extract claims and create JWTPrincipal
                    // We trust the token generated by our own config in tests
                    JWTPrincipal(credential.payload)
                }
            }
        }

        routing {
            authenticate("auth-jwt") {
                orderRoutes(mockOrderRepository, mockOrderItemRepository, mockProductRepository, mockUserRepository)
            }
        }
    }

    // --- Helper for setting JWT header ---
    private fun HttpRequestBuilder.jwtAuth(token: String = defaultTestToken) {
        header(HttpHeaders.Authorization, "Bearer $token")
    }

    // --- Tests ---

    @Test
    fun `GET _api_orders_{id} should return order when exists and user is owner`() {
        testApplication {
            application {
                testModule()
            }

            // Order belongs to testUserId (1), token is for testUserId (1)
            coEvery { mockOrderRepository.findById(1) } returns Result.success(testOrder)
            coEvery { mockOrderItemRepository.findByOrderId(1) } returns Result.success(listOf(testOrderItem))

            val response = client.get("/api/orders/1") {
                jwtAuth() // Use default token for user 1
            }
            assertEquals(HttpStatusCode.OK, response.status)
            assertTrue(response.bodyAsText().contains("Credit Card"))
        }
    }

    @Test
    fun `GET _api_orders_{id} should return 403 when user is not owner and not admin`() {
        testApplication {
            application {
                testModule()
            }

            // Order belongs to user 1, but token is for user 2
            val differentUserToken = createTestToken(userId = 2)
            coEvery { mockOrderRepository.findById(1) } returns Result.success(testOrder) // Order still belongs to user 1

            val response = client.get("/api/orders/1") {
                jwtAuth(differentUserToken)
            }
            assertEquals(HttpStatusCode.Forbidden, response.status)
        }
    }

